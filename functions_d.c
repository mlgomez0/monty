#include "monty.h"
/**
 *fun_stack - set the format to LIFO
 *@stack: doble pointer to the doubly linked list
 *@line_number:number to be pushed in the node
 */
void fun_stack(stack_t **stack, unsigned int line_number)
{
	free_all = 2;
	line_number = line_number;
	stack = stack;
}

/**
 *fun_queue - set the format to FIFO
 *@stack: doble pointer to the doubly linked list
 *@line_number:number to be pushed in the node
 */
void fun_queue(stack_t **stack, unsigned int line_number)
{
	free_all = 3;
	line_number = line_number;
	stack = stack;
}
